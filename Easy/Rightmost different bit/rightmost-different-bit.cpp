class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
         int cnt=0;
        while(n>0 or m>0){
            cnt++;
            if((n%2==0 and m%2) or (n%2 and m%2==0)) return cnt;
            n/=2;
            m/=2;
        }
        return -1;
    }
};

